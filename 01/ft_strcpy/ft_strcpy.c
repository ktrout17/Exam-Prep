#include <stdio.h>

char    *ft_strcpy(char *s1, char *s2)
{
    int     i;
    char    *str1;
    char    *str2;

    i = 0;
    str1 = (char *)s1;
    str2 = (char *)s2;
    while (s2)
    {
        str1[i] = str2[i];
        i++;
    }
    str1[i] = '\0';
    return (s1);
}